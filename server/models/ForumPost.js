import mongoose from 'mongoose';

const { Schema } = mongoose;

const forumPostSchema = new Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  author: {
    type: Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const ForumPost = mongoose.model('ForumPost', forumPostSchema);

export { ForumPost };